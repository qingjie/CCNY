public class Category {
	private int Id;
	private String Name;

	/**
	 * @return
	 */
	public int getId() {
		return Id;
	}

	/**
	 * @param id
	 */
	public void setId(int id) {
		Id = id;
	}

	/**
	 * @return
	 */
	public String getName() {
		return Name;
	}

	/**
	 * @param name
	 */
	public void setName(String name) {
		Name = name;
	}

}
